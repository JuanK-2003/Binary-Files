    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        i = Search.searchByID(jTextField1.getText());
        if ( i >= 0 ) {
            setDataInTF(i);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if ( validateFields() ) {
            p.get(i).editPerson(
                            jTextField2.getText(),
                            jTextField1.getText(),
                            Instant.ofEpochMilli(jDateChooser1.getDate().getTime()).atZone(ZoneId.systemDefault()).toLocalDate()
            );
        }
        clearFields();
    }//GEN-LAST:event_jButton2ActionPerformed
    void clearFields() {
        jTextField2.setText("");
        jTextField3.setText("");
        jDateChooser1.setDate(null);
        i= -1;
    }
    boolean validateFields() {
        return !jTextField2.getText().isEmpty() &&
                !jTextField3.getText().isEmpty() &&
                jDateChooser1.getDate() != null;
    }
    
    void setDataInTF(int idx){
        jTextField2.setText(p.get(idx).getID());
        jTextField3.setText(p.get(idx).getName());
        jDateChooser1.setDate(Date.from(p.get(idx).getBirthDate().atStartOfDay(ZoneId.systemDefault()).toInstant()));
    }

